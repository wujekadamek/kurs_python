# operatory przypisania

a = 12

a += 1 ; print(a) # <-- to samo, co a = a + 1      13
a -= 1 ; print(a)  # <-- to samo, co a = a - 1      12
a *= 2 ; print(a)  # <-- to samo, co a = a * 2      24
a /= 2 ; print(a)  # <-- to samo, co a = a / 2      12
a %= 2 ; print(a)  # <-- to samo, co a = a % 2      0
a += 3 ; print(a) # 4
a **= 3 ; print(a) # 27
a //= 20 ; print(a) # 1